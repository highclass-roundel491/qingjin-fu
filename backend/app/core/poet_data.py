def get_poet_relations(name: str) -> list[dict]:
    return [r for r in POET_RELATIONS if r["source"] == name or r["target"] == name]


def get_relation_label(source: str, target: str) -> str | None:
    for r in POET_RELATIONS:
        if (r["source"] == source and r["target"] == target) or \
           (r["source"] == target and r["target"] == source):
            return r["label"]
    return None


